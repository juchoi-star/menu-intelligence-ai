"""벌툰 분류(대분류) 중 '메뉴/음료' 와 '비메뉴(시간제·이용권 등)' 구분.

벌툰 매출의 대부분은 시간제/이용권 등 매장 이용요금이라, 메뉴개발 관점에서는
메뉴·음료만 보는 것이 맞다. 시간·요금·부대장비 관련 분류는 분석에서 제외한다.
(신규 분류에도 대응하도록 이름 키워드로 판별)
"""

from __future__ import annotations

# 비메뉴(제외) 판별 키워드 — 분류명에 하나라도 포함되면 제외
NON_MENU_KEYWORDS: tuple[str, ...] = (
    "시간제",   # 시간제, 성인시간제, 학생시간제, 시간제&변경 ...
    "요금",     # 주간요금, 야간요금, 추가요금, 요금제
    "이용권",   # 종일권 등 이용권류
    "진동벨",   # 진동벨(대여 장비)
    "방번호",
    "대여",     # 대여(충전기, 마사지, 닌텐도)
    "바코드",   # 바코드상품(리테일)
    "MD상품",   # 굿즈/리테일
    "주차",     # 주차권
    "보증금",
)


def is_menu_category(name: str | None) -> bool:
    """메뉴/음료 분류이면 True. 시간제·요금 등 비메뉴이면 False."""
    if not name:
        return True  # 미분류 등은 메뉴로 간주(보수적으로 유지)
    return not any(kw in name for kw in NON_MENU_KEYWORDS)
